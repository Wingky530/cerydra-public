export function getBentoPicks(bentoData: any[], userId: string) {
  const daySeed = Math.floor(Date.now() / 86400000);
  
  const hashString = (str: string) => {
    let hash = 0;
    for (let i = 0; i < str.length; i++) {
      hash = (hash << 5) - hash + str.charCodeAt(i);
      hash |= 0;
    }
    return Math.abs(hash);
  };
  
  const seed = daySeed + hashString(userId || '0');
  let s = seed;
  const random = () => {
    s = Math.sin(s) * 10000;
    return s - Math.floor(s);
  };

  const usedIds = new Set<number>();
  const picks = new Map<string, any>();
  
  const OFFICIAL_GENRES = new Set([
    'Action', 'Adventure', 'Comedy', 'Drama', 'Ecchi', 'Fantasy', 'Horror', 'Mahou Shoujo', 
    'Mecha', 'Music', 'Mystery', 'Psychological', 'Romance', 'Sci-Fi', 'Slice of Life', 'Sports', 'Supernatural', 'Thriller'
  ]);

  // Priority queue: Non-official tags and niche genres first, broad genres last.
  const getPriority = (genreName: string) => {
    if (!OFFICIAL_GENRES.has(genreName)) return 1; // Tags like Isekai, Space, Vampire
    if (['Mahou Shoujo', 'Mecha', 'Sports', 'Music', 'Horror'].includes(genreName)) return 2; // Niche official
    if (['Action', 'Comedy', 'Drama', 'Fantasy', 'Sci-Fi', 'Romance'].includes(genreName)) return 4; // Broadest
    return 3;
  };

  const sortedGenres = [...ALL_GENRES].sort((a, b) => getPriority(a.name) - getPriority(b.name));

  sortedGenres.forEach(genreObj => {
    let matches = bentoData.filter((anime: any) => {
      if (usedIds.has(anime.id)) return false;
      if (!anime.coverImage?.extraLarge) return false;

      const isGenre = anime.genres?.includes(genreObj.name);
      
      // If it's an official genre, strictly require it to be in anime.genres
      if (OFFICIAL_GENRES.has(genreObj.name)) {
        return isGenre;
      }

      // For non-official genres (tags), check the tags array
      const isTag = anime.tags?.some((t: any) => {
        if (t.name === genreObj.name) return true;
        if (genreObj.name === 'Isekai' && t.name.includes('Isekai')) return true;
        return false;
      });
      return isGenre || isTag;
    });

    if (matches.length > 0) {
      const randIndex = Math.floor(random() * matches.length);
      const picked = matches[randIndex];
      if (picked) {
        usedIds.add(picked.id);
        picks.set(genreObj.name.toLowerCase().replace(/\s+/g, '-'), picked);
        picks.set(genreObj.name, picked); // keep original name too
      }
    }
  });

  return picks;
}
